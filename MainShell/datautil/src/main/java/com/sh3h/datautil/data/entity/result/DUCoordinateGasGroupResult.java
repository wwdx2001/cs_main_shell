package com.sh3h.datautil.data.entity.result;



public class DUCoordinateGasGroupResult {
    private String X;
    private String Y;

    public DUCoordinateGasGroupResult(String x, String y) {
        X = x;
        Y = y;
    }

    public String getX() {
        return X;
    }

    public void setX(String x) {
        X = x;
    }

    public String getY() {
        return Y;
    }

    public void setY(String y) {
        Y = y;
    }
}
